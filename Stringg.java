public class Stringg {
    
    public static void main(String[] args) {
        
        //String functions

        String name ="Shivani";
        String namee=" Badola";
        System.out.println(name.length());  //length of string
        System.out.println(name + namee);  // to concatinate two strings
        System.out.println(name.charAt(6)); //to find character at a location
        System.out.println(name.indexOf("h")); //position of character in string
        System.out.println("Shivani \"The\" Badola"); //double inverted commas
        System.out.println("Shivani \'The\' Badola"); //single inverted commas
        System.out.println("Shivani \\ Badola");   //to add backslash(\)
        System.out.println("Shivani \n Badola"); // from new line
        System.out.println("Shivani \r"); //to return a value w3 school se example dekhna 
        System.out.println("Shiva\bni Badola"); //backspace
        
    }
}
