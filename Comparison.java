public class Comparison {
    
    public static void main(String[] args) {
        
        ////COMPARISON OPERATORS////

        int x =5;
        int y =3;
        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println(x>y);
        System.out.println(x<y);
        System.out.println(x>=y);
        System.out.println(x<=y);
    }
}
