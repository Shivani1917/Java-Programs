public class Logical {
    
    public static void main(String[] args) {
        
       ////LOGICAL OPERATORS////

       int x=5;
       System.out.println(x>3 && x<10);   //  both conditions are true
       System.out.println(x>3 || x<4);  // one condition is true
       System.out.println(!(x>3 && x<10));  // its false
    }
}
