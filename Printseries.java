public class Printseries {
    public static void main(String[] args) {
         
        System.out.println(" University Roll no:20105023");
        for(int n=5; n>0; n--){

            for(int i=1; i<=n; i++){
               System.out.print(i+ "\t");
            }
            System.out.println("");
        }
    }
}
